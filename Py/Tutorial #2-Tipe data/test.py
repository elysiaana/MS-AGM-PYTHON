# Tipe data: Angka no coma (interger)
data_interger = 1
print("data :", data_interger)
print("- bertipe :", type(data_interger))

# Tipe data: Kata (string)
data_string = "Elysia"
print("data :", data_string)
print("- bertipe :", type(data_string))

# Tipe data: Angka coma (float)
data_float = 1.5
print("data :", data_float)
print("- bertipe :", type(data_float))

# Tipe data: True and false (boolean)
data_boolean = False
print("data :", data_boolean)
print("- bertipe :", type(data_boolean))
